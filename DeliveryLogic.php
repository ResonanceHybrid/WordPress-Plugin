// Add JavaScript for dynamic updates
function nepal_checkout_scripts() {
    if (!is_checkout()) return;

    wp_enqueue_script('jquery');
    wp_register_script('nepal-checkout', '', ['jquery'], '', true);
    
    $script = '
    jQuery(document).ready(function($) {
        var locationData = ' . json_encode(get_nepal_location_data()) . ';
        
        function updateFields(prefix) {
            var province = $("#" + prefix + "_province");
            var district = $("#" + prefix + "_district");
            var area = $("#" + prefix + "_area");
            var postcode = $("#" + prefix + "_postcode");
            
            province.on("change", function() {
                var selectedProvince = $(this).val();
                district.empty().append("<option value=\"\">Select District</option>");
                area.empty().append("<option value=\"\">Select Area</option>");
                postcode.val("");
                
                if (selectedProvince && locationData[selectedProvince]) {
                    var districts = locationData[selectedProvince].districts;
                    Object.keys(districts).forEach(function(key) {
                        district.append("<option value=\"" + key + "\">" + districts[key].name + "</option>");
                    });
                }
                $(district).trigger("change");
            });
            
            district.on("change", function() {
                var selectedProvince = province.val();
                var selectedDistrict = $(this).val();
                area.empty().append("<option value=\"\">Select Area</option>");
                
                if (selectedDistrict && locationData[selectedProvince] && 
                    locationData[selectedProvince].districts[selectedDistrict]) {
                    var areas = locationData[selectedProvince].districts[selectedDistrict].areas;
                    Object.keys(areas).forEach(function(key) {
                        area.append("<option value=\"" + key + "\">" + areas[key].name + "</option>");
                    });
                    postcode.val(locationData[selectedProvince].districts[selectedDistrict].postal_code);
                }
                $(area).trigger("change");
            });
            
            area.on("change", function() {
                var selectedProvince = province.val();
                var selectedDistrict = district.val();
                var selectedArea = $(this).val();
                
                if (selectedArea && locationData[selectedProvince] && 
                    locationData[selectedProvince].districts[selectedDistrict] &&
                    locationData[selectedProvince].districts[selectedDistrict].areas[selectedArea]) {
                    var areaData = locationData[selectedProvince].districts[selectedDistrict].areas[selectedArea];
                    postcode.val(areaData.postal_code);
                }
            });
        }
        
        updateFields("billing");
        updateFields("shipping");
        
        $("#billing_province, #shipping_province").trigger("change");
        
        $("#billing_country, #shipping_country").on("change", function() {
            var prefix = $(this).attr("id").replace("_country", "");
            if ($(this).val() === "NP") {
                $("#" + prefix + "_province").trigger("change");
            }
        });
    });';

    wp_add_inline_script('nepal-checkout', $script);
    wp_enqueue_script('nepal-checkout');
}
add_action('wp_enqueue_scripts', 'nepal_checkout_scripts');