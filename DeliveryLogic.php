function nepal_hierarchical_checkout_fields($fields) {
    unset($fields['shipping']['shipping_city']);
    unset($fields['billing']['billing_city']);
    
    $country_priority = isset($fields['billing']['billing_country']['priority']) 
        ? $fields['billing']['billing_country']['priority'] 
        : 40;
    
    $location_fields = [
        'province' => [
            'type' => 'select',
            'label' => 'Province',
            'required' => true,
            'class' => ['form-row-wide', 'address-field', 'update_totals_on_change'],
            'clear' => true,
            'priority' => $country_priority + 1,
            'options' => [''=>'Select Province']
        ],
        'district' => [
            'type' => 'select',
            'label' => 'District',
            'required' => true,
            'class' => ['form-row-wide', 'address-field', 'update_totals_on_change'],
            'clear' => true,
            'priority' => $country_priority + 2,
            'options' => [''=>'Select District']
        ],
        'area' => [
            'type' => 'select',
            'label' => 'Area',
            'required' => true,
            'class' => ['form-row-wide', 'address-field', 'update_totals_on_change'],
            'clear' => true,
            'priority' => $country_priority + 3,
            'options' => [''=>'Select Area']
        ]
    ];

    foreach (get_nepal_location_data() as $province_key => $province) {
        $location_fields['province']['options'][$province_key] = $province['name'];
    }

    foreach (['billing', 'shipping'] as $field_type) {
        if (isset($fields[$field_type][$field_type . '_address_1'])) {
            $fields[$field_type][$field_type . '_address_1']['priority'] = $country_priority + 4;
        }
        if (isset($fields[$field_type][$field_type . '_address_2'])) {
            $fields[$field_type][$field_type . '_address_2']['priority'] = $country_priority + 5;
        }
        if (isset($fields[$field_type][$field_type . '_postcode'])) {
            $fields[$field_type][$field_type . '_postcode']['priority'] = $country_priority + 6;
        }

        foreach ($location_fields as $key => $field) {
            $fields[$field_type][$field_type . '_' . $key] = $field;
        }
    }

    return $fields;
}
add_filter('woocommerce_checkout_fields', 'nepal_hierarchical_checkout_fields');

// Add country-specific field display
function nepal_conditional_checkout_fields($fields) {
    $nepal_specific_fields = ['province', 'district', 'area'];
    
    foreach (['billing', 'shipping'] as $field_type) {
        foreach ($nepal_specific_fields as $field) {
            $field_id = $field_type . '_' . $field;
            if (isset($fields[$field_type][$field_id])) {
                $fields[$field_type][$field_id]['country'] = ['NP'];
            }
        }
    }
    
    return $fields;
}
add_filter('woocommerce_checkout_fields', 'nepal_conditional_checkout_fields');

// Format the address for display
function nepal_format_address_fields($address, $order) {
    if ($address['country'] === 'NP') {
        // Get the saved meta values
        $type = (strpos(current_filter(), 'billing') !== false) ? 'billing' : 'shipping';
        $province_key = get_post_meta($order->get_id(), "_{$type}_province", true);
        $district_key = get_post_meta($order->get_id(), "_{$type}_district", true);
        $area_key = get_post_meta($order->get_id(), "_{$type}_area", true);
        
        $location_data = get_nepal_location_data();
        
        if (!empty($province_key) && isset($location_data[$province_key])) {
            $province = $location_data[$province_key]['name'];
            
            if (!empty($district_key) && isset($location_data[$province_key]['districts'][$district_key])) {
                $district = $location_data[$province_key]['districts'][$district_key]['name'];
                
                if (!empty($area_key) && isset($location_data[$province_key]['districts'][$district_key]['areas'][$area_key])) {
                    $area = $location_data[$province_key]['districts'][$district_key]['areas'][$area_key]['name'];
                    
                    // Update the formatted address
                    $address['state'] = $province;
                    $address['city'] = $district;
                    
                    // Add area to address_1 if it's empty, otherwise append to existing address
                    if (empty($address['address_1'])) {
                        $address['address_1'] = $area;
                    } else {
                        $address['address_1'] = $area . ', ' . $address['address_1'];
                    }
                }
            }
        }
    }
    return $address;
}
add_filter('woocommerce_order_formatted_billing_address', 'nepal_format_address_fields', 10, 2);
add_filter('woocommerce_order_formatted_shipping_address', 'nepal_format_address_fields', 10, 2);

// Save the selected location data as order meta
function nepal_save_order_address_meta($order_id) {
    $order = wc_get_order($order_id);
    
    // Fields to save
    $address_types = ['billing', 'shipping'];
    $fields = ['province', 'district', 'area'];
    
    foreach ($address_types as $type) {
        foreach ($fields as $field) {
            $field_key = "_{$type}_{$field}";
            $field_value = isset($_POST["{$type}_{$field}"]) ? sanitize_text_field($_POST["{$type}_{$field}"]) : '';
            if ($field_value) {
                update_post_meta($order_id, $field_key, $field_value);
            }
        }
    }
}
add_action('woocommerce_checkout_update_order_meta', 'nepal_save_order_address_meta');

